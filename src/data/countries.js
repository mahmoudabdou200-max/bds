export const getDisasterTypeForCountry = (countryId) => {
  const country = countries.find(c => c.id === countryId);
  if (!country) return 'simulation';
  
  const { challenges } = country;
  
  if (challenges[Factor.QUAKE] >= 4) return 'earthquake';
  if (challenges[Factor.WATER] >= 4) return 'flood';
  if (challenges[Factor.HEAT] >= 4 && challenges.sandstorms >= 4) return 'sandstorm';
  if (challenges[Factor.HEAT] >= 4) return 'heat_wave';
  if (challenges[Factor.COLD_SNOW] >= 4) return 'blizzard';
  if (challenges[Factor.WIND] >= 4) return 'typhoon';
  
  return 'simulation';
};